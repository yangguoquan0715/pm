package com.gz.pm.util;

public enum Relationship {
	
	FATHER("父亲",1),
	MOTHER("母亲",2),
	SPOUSE("配偶",3),
	SON("儿子",4),
	DAUGHTER("女儿",5);
	
	private String name;
	private int value;
	
	private Relationship(String name, int value) {
		this.name = name;
		this.value = value;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	
}
